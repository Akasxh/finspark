"""Integration tests for the LLM-wired /configurations/generate endpoint.

Covers:
- LLM path when AI_ENABLED=True and GEMINI_API_KEY is set
- Fallback to rule-based when LLM raises
- Rule-based path when AI_ENABLED=False
- Retry logic: LLM retried up to 3 times before fallback
- generation_method tag in response payload
"""
from __future__ import annotations

import uuid
from typing import Any
from unittest.mock import AsyncMock, MagicMock, patch

import pytest

from finspark.api.v1.endpoints.configurations import (
    _MAX_LLM_RETRIES,
    _llm_generate_with_retry,
    _rule_based_generate,
    generate_configuration,
)
from finspark.schemas.configurations import ConfigGenerateRequest, ConfigStatus
from finspark.services.llm.client import GeminiAPIError

pytestmark = pytest.mark.unit


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------


def _make_generate_request(**kwargs: Any) -> ConfigGenerateRequest:
    defaults = {
        "tenant_id": uuid.uuid4(),
        "adapter_id": uuid.uuid4(),
        "document_ids": [],
        "overrides": {},
        "llm_hint": "test hint",
    }
    defaults.update(kwargs)
    return ConfigGenerateRequest(**defaults)


_SAMPLE_LLM_PAYLOAD: dict[str, Any] = {
    "base_url": "https://api.example.com",
    "endpoints": [{"path": "/score", "method": "POST", "description": "Credit score"}],
    "auth": {"type": "api_key", "config": {"key_name": "X-API-Key", "in": "header"}},
    "timeout_ms": 3000,
    "retry_count": 3,
    "retry_backoff": "exponential",
    "field_mappings": [
        {"source_field": "customer.pan", "target_field": "pan_number", "transform": None, "required": True}
    ],
    "headers": {"Content-Type": "application/json"},
    "notes": "Generated by Gemini",
}


