    @pytest.mark.asyncio
    async def test_generation_path_recorded_in_config(self) -> None:
        """_generation_path key is persisted into full_config."""
        from finspark.api.routes.configurations import generate_configuration
        from finspark.schemas.configurations import GenerateConfigRequest

        request = GenerateConfigRequest(
            document_id="doc-1",
            adapter_version_id="av-1",
            name="Test Config",
        )

        import json

        db = self._make_mock_db(self._make_mock_doc(), self._make_mock_av())
        added_config: dict[str, Any] = {}

        original_add = db.add

        def capture_add(obj: Any) -> None:
            nonlocal added_config
            if hasattr(obj, "full_config") and obj.full_config:
                added_config = json.loads(obj.full_config)
            original_add(obj)

        db.add = capture_add

        with patch("finspark.api.routes.configurations.settings") as mock_settings:
            mock_settings.ai_enabled = False
            mock_settings.gemini_api_key = ""

            await generate_configuration(
                request=request,
                background_tasks=BackgroundTasks(),
                db=db,
                tenant=self._make_mock_tenant(),
                generator=ConfigGenerator(),
                audit=self._make_mock_audit(),
            )

        assert "_generation_path" in added_config
        assert added_config["_generation_path"] == "rule_based"

