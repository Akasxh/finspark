class TestGenerateConfigurationPipeline:
    """Tests for the full pipeline via mocked settings + DB."""

    def _make_mock_doc(self, parsed_result: dict[str, Any] | None = None) -> MagicMock:
        doc = MagicMock()
        doc.parsed_result = __import__("json").dumps(parsed_result or _make_parsed_result())
        doc.filename = "test.pdf"
        return doc

    def _make_mock_av(self) -> MagicMock:
        av = MagicMock()
        av.adapter_id = "test-adapter"
        av.version = "v1"
        av.base_url = "https://api.example.com"
        av.auth_type = "api_key"
        av.endpoints = None
        av.request_schema = None
        av.response_schema = None
        return av

    def _make_mock_db(self, doc: MagicMock, av: MagicMock) -> AsyncMock:
        """DB session that returns the given doc and adapter version on execute.

        Also patches Configuration.__init__ so that flush() fills in id/timestamps
        on any Configuration object added to the session.
        """
        import uuid
        from datetime import UTC, datetime

        db = AsyncMock()

        def _make_result(scalar_value: Any) -> MagicMock:
            result = MagicMock()
            result.scalar_one_or_none.return_value = scalar_value
            return result

        db.execute = AsyncMock(
            side_effect=[_make_result(doc), _make_result(av)]
        )

        def _add_and_fill(obj: Any) -> None:
            """Set id + timestamps on Configuration instances so pydantic validation passes."""
            from finspark.models.configuration import Configuration
            if isinstance(obj, Configuration):
                if not obj.id:
                    obj.id = str(uuid.uuid4())
                if not obj.created_at:
                    obj.created_at = datetime.now(UTC)
                if not obj.updated_at:
                    obj.updated_at = datetime.now(UTC)

        db.add = MagicMock(side_effect=_add_and_fill)
        db.flush = AsyncMock()
        return db

    def _make_mock_tenant(self) -> MagicMock:
        t = MagicMock()
        t.tenant_id = "tenant-1"
        t.tenant_name = "Test Tenant"
        return t

    def _make_mock_audit(self) -> AsyncMock:
        audit = AsyncMock()
        audit.log = AsyncMock()
        return audit

    @pytest.mark.asyncio
    async def test_rule_based_path_when_ai_disabled(self) -> None:
        """When AI is disabled, rule-based generator is used directly."""
        from finspark.api.routes.configurations import generate_configuration
        from finspark.schemas.configurations import GenerateConfigRequest

        request = GenerateConfigRequest(
            document_id="doc-1",
            adapter_version_id="av-1",
            name="Test Config",
        )

        with patch("finspark.api.routes.configurations.settings") as mock_settings:
            mock_settings.ai_enabled = False
            mock_settings.gemini_api_key = ""

            result = await generate_configuration(
                request=request,
                background_tasks=BackgroundTasks(),
                db=self._make_mock_db(self._make_mock_doc(), self._make_mock_av()),
                tenant=self._make_mock_tenant(),
                generator=ConfigGenerator(),
                audit=self._make_mock_audit(),
            )

        assert result.data.status == "configured"
        assert result.data.version == 1
        payload = result.message
        assert "rule_based" in payload

