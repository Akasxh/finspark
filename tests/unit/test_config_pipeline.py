class TestAugmentWithRuleBased:
    def test_confidence_overridden_by_rule_based(self, generator: ConfigGenerator) -> None:
        """Rule-based confidence replaces LLM's self-assessed confidence."""
        parsed = _make_parsed_result()
        av_dict = _make_av_dict()

        # LLM gives confidence 0.5 for pan_number -> pan
        llm_config: dict[str, Any] = {
            "field_mappings": [
                {
                    "source_field": "pan_number",
                    "target_field": "pan",
                    "confidence": 0.5,
                    "is_confirmed": False,
                    "transformation": None,
                }
            ]
        }

        result = _augment_with_rule_based(llm_config, parsed, av_dict, generator)
        mappings = result["field_mappings"]
        pan_mapping = next(
            (m for m in mappings if m.get("source_field") == "pan_number"), None
        )
        assert pan_mapping is not None
        # Rule-based synonym match for pan_number -> pan_number gives 1.0
        assert pan_mapping["confidence"] == 1.0

    def test_backfills_unmapped_source_fields(self, generator: ConfigGenerator) -> None:
        """Fields present in rule-based but absent from LLM are appended."""
        parsed = _make_parsed_result()
        av_dict = _make_av_dict()

        # LLM only mapped pan_number; rule-based will also map mobile_number
        llm_config: dict[str, Any] = {
            "field_mappings": [
                {
                    "source_field": "pan_number",
                    "target_field": "pan_number",
                    "confidence": 0.8,
                    "is_confirmed": True,
                    "transformation": None,
                }
            ]
        }

        result = _augment_with_rule_based(llm_config, parsed, av_dict, generator)
        source_fields = [m.get("source_field") for m in result["field_mappings"]]
        assert "pan_number" in source_fields
        # mobile is in parsed_result fields — rule-based may find it
        # (at minimum, pan_number must be preserved)
        assert len(result["field_mappings"]) >= 1

    def test_preserves_llm_base_url_and_endpoints(self, generator: ConfigGenerator) -> None:
        """Non-field_mappings keys from LLM config are preserved unchanged."""
        parsed = _make_parsed_result()
        av_dict = _make_av_dict()

        result = _augment_with_rule_based(_SAMPLE_LLM_CONFIG.copy(), parsed, av_dict, generator)

        assert result["base_url"] == _SAMPLE_LLM_CONFIG["base_url"]
        assert result["timeout_ms"] == _SAMPLE_LLM_CONFIG["timeout_ms"]
        assert result["retry_count"] == _SAMPLE_LLM_CONFIG["retry_count"]
        assert result["notes"] == _SAMPLE_LLM_CONFIG["notes"]

    def test_empty_llm_mappings_filled_by_rule_based(self, generator: ConfigGenerator) -> None:
        """When LLM returns no field_mappings, rule-based fills them."""
        parsed = _make_parsed_result()
        av_dict = _make_av_dict()

        llm_config: dict[str, Any] = {
            "base_url": "https://api.example.com",
            "field_mappings": [],
        }

        result = _augment_with_rule_based(llm_config, parsed, av_dict, generator)
        # Rule-based should have produced at least one mapping for pan_number
        assert len(result["field_mappings"]) >= 1

    def test_generation_path_key_not_added_by_augment(self, generator: ConfigGenerator) -> None:
        """_augment_with_rule_based does not add _generation_path; caller does."""
        parsed = _make_parsed_result()
        av_dict = _make_av_dict()
        result = _augment_with_rule_based({}, parsed, av_dict, generator)
        assert "_generation_path" not in result


